// Utilidades de criptografía — SHA-256 hex de UTF-8, compatible con el ERP web.
// Implementación 100% JavaScript pura: funciona idéntico en web, Hermes (Expo Go),
// y standalone (iOS/Android), sin depender de crypto.subtle ni de módulos nativos.
// Produce el MISMO hash hex que el ERP (frontend/src/services/authAPI.js).

// ---------------- SHA-256 puro ----------------
// Basado en FIPS 180-4. Acepta string UTF-8 y devuelve hex en minúsculas.

